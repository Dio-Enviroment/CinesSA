	final static String duracion(int min) { 
		String hora;
		int HH,MM;
		HH=min/60;
		MM=min%60;
		if (HH>=10) {
			if (MM==0) {
				hora=(HH+":"+MM+"0");
			}else {
				hora=(HH+":"+MM);
			}
			
		} else {
			if (MM==0) {
				hora=("0"+HH+":"+MM+"0");
			}else {
				hora=(HH+":"+MM);
			}
		}
		return hora;
	}
	
	final static String horarios() {
		String [] horas = { "09:30","12:00","14:00","15:30","16:20","17:00","18:00","19:00","20:00"};
		int in=0,k=0,i=0;
		String [] l=new String [4];
		String d="Sin Horario";
		do {
			k=0;
			while (k<4){
				in=(int)(Math.random()*horas.length-1);
				l[k]=horas[in];
				k++;
			}
			if (l[0]!=l[1]&&l[0]!=l[2]&&l[0]!=l[3]) {
				if (l[1]!=l[0]&&l[1]!=l[2]&&l[1]!=l[3]) {
					if (l[2]!=l[0]&&l[2]!=l[1]&&l[2]!=l[3]) {
						if (l[3]!=l[0]&&l[3]!=l[1]&&l[3]!=l[2]) {
							d=(l[0]+" | "+l[1]+" | "+l[2]+" | "+l[3]);
							i=l.length;
						}
					}
				}
			}
		} while (i<l.length);
		return d;
	}

