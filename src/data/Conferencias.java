	static String duracion(int min) { 
		String hora;
		int HH,MM;
		HH=min/60;
		MM=min%60;
		if (HH>=10) {
			if (MM==0) {
				hora=(HH+":"+MM+"0");
			}else {
				hora=(HH+":"+MM);
			}
			
		} else {
			if (MM==0) {
				hora=("0"+HH+":"+MM+"0");
			}else {
				hora=(HH+":"+MM);
			}
		}
		return hora;
	}

